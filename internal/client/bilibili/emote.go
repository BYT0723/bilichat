func ReplaceEmoteCodes(text string) string {
	return emotePattern.ReplaceAllStringFunc(text, func(match string) string {
		code := emotePattern.FindStringSubmatch(match)[1]
		if repl, ok := emoteMap[code]; ok {
			return repl + match
		}
		return match
	})
}
